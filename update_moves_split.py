def update_battle_moves():
    with open('/home/chale/pokemon-fire-red/pokefirered/src/data/battle_moves.h', 'r') as f:
        content = f.read()
    
    # Pattern to match move blocks
    pattern = r'(\[MOVE_[A-Z_]+\]\s*=\s*\{[^}]+\.flags\s*=\s*[^,}]+),(\s*\})'
    
    def replace_move(match):
        move_block = match.group(1)
        closing = match.group(2)
        
        # Extract move name
        move_match = re.search(r'\[MOVE_([A-Z_]+)\]', move_block)
        if move_match:
            move_name = 'MOVE_' + move_match.group(1)
            split_value = move_splits.get(move_name, 'SPLIT_STATUS')  # Default to status if not found
            return f"{move_block},\n        .split = {split_value},{closing}"
        
        return match.group(0)
    
    # Apply the replacement
    updated_content = re.sub(pattern, replace_move, content, flags=re.DOTALL)
    
    with open('/home/chale/pokemon-fire-red/pokefirered/src/data/battle_moves.h', 'w') as f:
        f.write(updated_content)
    
    print("Updated battle_moves.h with split fields")

if __name__ == "__main__":
    update_battle_moves()
